/**
 * Answer-state interpretation for industry depth questions.
 * Mirrors `interpretAnswerState` from gearMetricRegistry but kept local
 * so future IB-H4 surfaces can compose without circular imports.
 *   - "unknown"    -> visibility weakness
 *   - "incomplete" -> system-exists-but-slips
 *   - "no"         -> slipping gear / failing tooth
 *   - "verified"   -> stable
 */
export function interpretIndustryDepthAnswer(state: AnswerState): {
  isStable: boolean;
  isVisibilityWeakness: boolean;
  isSlipping: boolean;
} {
  switch (state) {
    case "verified":
      return { isStable: true, isVisibilityWeakness: false, isSlipping: false };
    case "incomplete":
      return { isStable: false, isVisibilityWeakness: false, isSlipping: true };
    case "unknown":
      return { isStable: false, isVisibilityWeakness: true, isSlipping: true };
    case "no":
      return { isStable: false, isVisibilityWeakness: false, isSlipping: true };
  }
}
