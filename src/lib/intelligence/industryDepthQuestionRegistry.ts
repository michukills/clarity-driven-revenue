function answerStates(label: string): IndustryDepthAnswerStateLogic {
  return {
    verified: `Owner produced concrete evidence for: ${label}.`,
    incomplete: `${label} exists but is not consistently followed or measured; gear still slips.`,
    unknown: `Owner does not know / not tracked. Treated as visibility weakness, NOT neutral.`,
    no: `No process exists for: ${label}. Slipping gear / failing tooth.`,
  };
}

